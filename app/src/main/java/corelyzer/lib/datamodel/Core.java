package corelyzer.lib.datamodel;

/**
 * Defines the interface for core-related properties.
 * 
 * @author Josh Reed (jareed@andrill.org)
 */
public interface Core {

	/**
	 * Gets the depth of this core.
	 * 
	 * @return the depth.
	 */
	double getDepth();

	/**
	 * Gets the length of this core.
	 * 
	 * @return the length.
	 */
	double getLength();
}
